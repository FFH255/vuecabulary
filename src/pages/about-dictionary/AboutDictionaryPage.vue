        В. И. Чернышев тщательно сверил первоначальные журнальные тексты очерков с их текстами более позднего времени и выявил фонетические, лексические, грамматические, орфографические черты, отличавшие их язык от литературного языка той эпохи. Лексические и фразеологические явления тульской городской мещанской речи, взятые  из первоначальных журнальных текстов очерковых циклов Г. И. Успенского «Нравы Растеряевой улицы» и «Из деревенского дневника», вошли в словарь. Они имеют двойную авторскую помету источника – Г. Успенский (с названием очеркового цикла) и Чернышев, показывающую, что языковые единицы были извлечены В. И. Чернышевым из первоначального журнального текста очерков.<br/>
