        В некоторых случаях словарные статьи не содержат иллюстративного материала. Как правило, это касается устойчивых сочетаний следующих видов:<br/>
        1) зафиксированных В. И. Далем как тульские без иллюстраций (в таком случае используется помета Отмечено Далем как тульское);<br/>
        2) отраженных старым словником кафедры русского языка ТГПИ–ТГПУ им. Л.Н. Толстого (к ним в ходе экспедиций не было найдено иллюстративного материала по причине их выхода из употребления в указанных населенных пунктах или районах);<br/>
        3) приведенных СРНГ как тульские без иллюстраций (помета Отмечено СРНГ как тульское);
