        В ходе создания словаря осуществлялась систематическая сверка его словника с корпусами «Словаря русских народных говоров» и «Толкового словаря живого великорусского языка» В.И. Даля. Устойчивые сочетания слов, зафиксированные в прошлые десятилетия составителями СРНГ на территории Тульского региона и имеющие помету Тул., включаются в словарь с указанием: <i>Отмечено СРНГ как тульское</i>. Аналогичные сочетания из Словаря В. И. Даля маркируются пометой <i>Отмечено Далем как тульское.</i><br/>
        Как правило, это те сочетания, которые в настоящее время уже не отмечаются в речи носителей тульских говоров или пока не обнаружены в ходе экспедиционной работы (поэтому в словаре они могут не сопровождаться информацией о конкретной локализации). Исключение составляют те сочетания, которые снабжены конкретной ареальной пометой в СРНГ и у В. И. Даля.<br/>
        <strong>Иллюстративный материал.</strong> В качестве иллюстративного материала в «Словаре тульских говоров» используются не только записи живой разговорной речи, но и фольклорные тексты, зафиксированные в разное время студенческими группами ТГПУ в ходе диалектологических и фольклорных практик.<br/>
