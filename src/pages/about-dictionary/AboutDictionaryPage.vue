        Данный иллюстративный источник заслуживает отдельного комментария. Тексты очерковых циклов Г. И. Успенского имеют непростую судьбу. Впервые они были опубликованы в известных демократических журналах 1860–1870-х гг. XIX в.: «Нравы Растеряевой улицы» – в журнале «Современник» (книги II–III за 1866 г.), «Из деревенского дневника» – в журнале «Отечественные записки» (книга IX за 1878 г.). По замечанию В. И. Чернышева, «до Глеба Ивановича Успенского в литературе были попытки представить народную, собственно крестьянскую, областную речь, но областную речь мещан, речь городского пролетариата немногие передавали с такой полнотой, точностью и меткостью, с какой они переданы в его очерках…» [Чернышев 1970: 297].<br/>
