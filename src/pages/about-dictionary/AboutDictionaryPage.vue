        <strong>Общий принцип построения словаря</strong> соответствует лингвистической традиции подобного рода изданий и является алфавитным. Первые слова устойчивых сочетаний располагаются в алфавитном порядке и организуют основной корпус словаря.<br/>
        «Словарь тульских говоров» носит дифференциальный характер: в него входят преимущественно диалектные устойчивые сочетания слов, отсутствующие в литературном языке или имеющие какие-либо отличия от соответствующих литературных (семантические, структурные, функциональные и проч.). Диалектное варьирование общеязыковых устойчивых сочетаний слов представлено в словаре достаточно широко. Как уже отмечалось, словарь отчасти фиксирует городское просторечие областного центра. <br/>
        Структура словарной статьи. Словарная статья содержит следующие зоны информации: 1) заголовочное сочетание с указанием ударения; 2) стилистическая, функциональная или грамматическая помета; 3) толкование значения; 4) указание на территорию распространения сочетания; 5) иллюстрация употребления; 6) место фиксации приведенного примера.<br/>
        В качестве заголовка статьи выступает начальная грамматическая форма сочетания. Ударение приводится только над словами, которые могут быть по-разному истолкованы или не являются широко известными. Над прочими компонентами сочетаний (в том числе односложными) ударение не ставится.<br/>
        Варианты компонентного состава устойчивых сочетаний слов помещаются в круглых скобках после варьирующегося структурного элемента. Аналогично подается орфографическое и морфемное варьирование.<br/>
        Необязательные компоненты устойчивых сочетаний, которые тем не менее могут в них присутствовать, даются в ломаных скобках.<br/>
        Полисемия нумеруется внутри одной словарной статьи. Для каждого значения многозначного сочетания приводится его толкование. Иллюстрации даются к каждому значению полисемичной структуры лишь тогда, когда зафиксирован соответствующий языковой материал. В противном случае пример помещается после нумерованных значений и соответствует одному из них.<br/>
        В семантической зоне словарной статьи введено указание оттенков (вариантов) значения. Оттенки даются через две косолинейные черты – // – и не нумеруются. Они не сопровождаются отдельными иллюстрациями и указаниями на зону локализации. В иллюстративной части статьи может быть дан пример, показывающий реализацию как основного значения, так и его оттенков. Если нет специальных указаний, территориальная локализация общего значения сочетания характерна и всем его оттенкам.<br/>
        Отсылочная помета См. сопровождает редкие устойчивые сочетания, форма или семантика которых лучше осознается при сравнении с сочетанием, к которому дается отсылка.<br/>
        При необходимости (если это важно для понимания семантики сочетания) указывается переносный характер значения идиомы, сфера употребления сочетания, область его функционирования или стилистическая окраска. Названные семантические особенности регламентируются пометами перен. (переносное), промысл. (промысловое), обряд. (обрядовое), неодобр. (неодобрительное) и др. У примеров из фольклора (фольк.) отмечается также жанр (песня, сказка, байка и т. п.). <br/>
        В словарной статье указываются районы Тульской области, в которых сочетание употребляется. Если сочетание многозначно, такая информация дается для каждого значения. Территориальная характеристика собрана по всем источникам, доступным авторам.<br/>
        История административного деления территории тульских говоров вкратце сводится к следующему. По административной реформе Петра I 1708 г. в Росси было учреждено 8 губерний. Большая чсасть территории, занимаемой тульскими говорами, вошла в Тульскую провинцию Московской гебернии (Тула, Венев, Богородицк, Крапивна, Епифань, Дедилов).<br/>
        При образовании Тульского наместничества в 1777 г. сюда вошли 12 уездов: Алексинский, Белевский, Богородицкий, Веневский, Епифанский, Ефремовский, Каширский, Крапивенский, Новосильский, Одоевский, Тульский и Чернский. <br/>
        В 1796–1797 гг. наместничества были переименованы в губернии, большинство уездов Тульской губернии сохранились в ее составе. <br/>
        После Октябрьской революции, в 1923 г., Каширский уезд был передан в состав Московской губернии. В 1925 г. Новосильский уезд присоединен к Орловской губернии. <br/>
        В 1929 г. губернии были ликвидированы. Эта территория вошла в состав Тульского округа Московской области. После ликвидации округов в 1930 г. вся территория была присоединена к Московской области.<br/>
        В 1937 г. была выделена Тульская область, состав которой в военное и послевоенное время несколько менялся. В 1957 г. в состав области дополнительно вошли Сталиногорский (ныне Новомосковский), Узловской, Донской, Кимовский и Гремячевский районы.<br/>
        В настоящее время в состав области входят 23 района, охватывющие зону тульских говоров (список районов приведен непосредственно перед словарем). Ленинский район, в 2014 г. переставший быть муниципальным районом, сохраняет статус административно-территориальной единицы области. Последняя особенность отражена в словаре: сочетания, записанные от жителей населенных пунктов бывшего Ленинского района, снабжены территориальной пометой Лен. <br/>
        Иллюстрации употребления сочетания выделяются курсивом. Где это не мешает пониманию, в примерах отражаются фонетические особенности речи информантов. После примера указывается конкретный населенный пункт, где употребление было зафиксировано.<br/>
        Помета Повсеместно характеризует либо широкую локализацию сочетания в тульских говорах, либо его типичное употребление.<br/>
        Источниковые пометы СРНГ и Даль сопровождают иллюстрации, заимствованные из «Словаря русских народных говоров» и  «Толкового словаря живого великорусского языка» В. И. Даля.<br/>
        Устойчивые сочетания слов Тульского края, отмеченные В.И. Далем и составителями СРНГ в позапрошлом и прошлом веках, представляют собой очень ценный и почти не сохраненный в настоящее время живой речью материал.<br/>


